class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right

# Create two binary trees in the global scope
# Tree 1:
#     3
#    / \
#   5   1
tree1 = TreeNode(3)
tree1.left = TreeNode(5)
tree1.right = TreeNode(1)


class Solution:
    def goodNodes(self, r, ma=-10000):
        return self.goodNodes(r.left, max(ma, r.val)) + self.goodNodes(r.right, max(ma, r.val)) + (r.val >= ma) if r else 0

ic(Solution().goodNodes(tree1, -10000))

